let { add, minus } = require("./math");

describe("测试add方法", () => {
  it("测试2+4=6", () => {
    assert(2 + 4 === 6, "2+4!==6");
  });
  it("测试3+6=9", () => {
    assert(3 + 6 === 9, "3+6!==9");
  });
});

describe("测试minus方法", () => {
  it("测试2-4=-2", () => {
    assert(2 - 4 === -2, "2-4!==-2");
  });
  it("测试3-6=-3", () => {
    assert(3 - 6 === -3, "3-6!==-3");
  });
});
